export function getSeedByType(type: Content["contentType"]): Content[] {
  return SEED_CONTENT.filter((c) => c.contentType === type);
}

export function getSeedBySlug(slug: string): Content | undefined {
  return SEED_CONTENT.find((c) => c.slug === slug || c.id === slug);
}

export function searchSeed(query: string): Content[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return SEED_CONTENT.filter((c) => {
    const fields = [
      c.title,
      c.originalTitle,
      c.englishTitle,
      c.romajiTitle,
      c.nativeTitle,
      ...(c.alternateTitles ?? []),
    ]
      .filter(Boolean)
      .map((s) => (s as string).toLowerCase());
    return fields.some((f) => f.includes(q));
  });
}
